public class Detail {
    private String location;
    private String simpleCue;
    private String detailCue;

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getSimpleCue() {
        return simpleCue;
    }

    public void setSimpleCue(String simpleCue) {
        this.simpleCue = simpleCue;
    }

    public String getDetailCue() {
        return detailCue;
    }

    public void setDetailCue(String detailCue) {
        this.detailCue = detailCue;
    }
}
